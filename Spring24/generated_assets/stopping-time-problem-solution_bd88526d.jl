function construct_a(m)
	(; N_a, a‚Çò·µ¢‚Çô, a‚Çò‚Çê‚Çì) = m
	[a·µ¢ for a·µ¢ in range(a‚Çò·µ¢‚Çô, a‚Çò‚Çê‚Çì, N_a)] |> hcat # asset grid (column vector)
